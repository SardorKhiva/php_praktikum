<?php
// Inkrement operatori:
$a = 5;
$b = 5;

// avval qiymat +1 ga oshirilib keyin chiqariladi:
echo ++$a . PHP_EOL;

// oldin chiqarilib,
echo $b++ . PHP_EOL;

// keyin +1 qilib chiqariladi:
echo $b . PHP_EOL;
echo PHP_EOL;




// Dekrement operatori:
$c = 10;
$d = 10;

// avval -1 bo'lib keyin chiqariladi:
echo --$c . PHP_EOL;

// avval avvalgi qiymati chiqariladi:
echo $d-- . PHP_EOL;
// keyin -1 qilib chiqariladi:
echo $d . PHP_EOL;