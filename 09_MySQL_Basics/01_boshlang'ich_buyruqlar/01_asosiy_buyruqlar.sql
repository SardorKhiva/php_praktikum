/*
    mysql -u root -p
    -u - user - foydalanuvchi
    root - foydalanuvchi_nomi
    -p - baza paroli

    mysql: Это основная команда для запуска клиента MySQL.

-u: Этот флаг означает "user" (пользователь).

root: Это имя пользователя с правами администратора. Обычно это главный пользователь по умолчанию, который имеет полный доступ ко всем базам данных.

-p: Этот флаг означает "password" (пароль). После ввода команды система предложит вам ввести пароль. Это самый безопасный способ, так как пароль не будет виден в истории команд.

    mysql -u sardo684_root -p -h 213.230.86.39 -P 3306 --default-character-set=utf8mb4 --connect-timeout=10 --show-warnings -e "SELECT * FROM users"

    --default-character-set=utf8mb4
Назначение: Устанавливает кодировку для соединения.

Функция: Эта опция гарантирует, что все данные, которые вы отправляете или получаете, будут обрабатываться с использованием указанной кодировки. utf8mb4 — это наиболее предпочтительная кодировка для современных баз данных, так как она полностью поддерживает Unicode, включая смайлики и редкие символы. Это предотвращает проблемы с некорректным отображением текста.

--connect-timeout=10
Назначение: Устанавливает максимальное время ожидания для подключения к серверу.

Функция: Эта опция позволяет клиенту MySQL ждать 10 секунд, прежде чем прекратить попытку подключения. Если сервер недоступен или сильно нагружен, клиент не будет бесконечно "зависать", а вернет ошибку. Это особенно полезно при автоматизации скриптов.

--show-warnings
Назначение: Показывает предупреждения, которые генерирует сервер.

Функция: Даже если SQL-запрос выполнен успешно, сервер может сгенерировать предупреждения, например, о неполном совпадении типов данных. Этот флаг заставляет клиент отображать эти предупреждения, что очень полезно для отладки и понимания, как именно MySQL обрабатывает ваши запросы.

-e "SELECT * FROM users"
Назначение: Выполняет команду без входа в интерактивную оболочку.

Функция: Флаг -e (сокращение от --execute) позволяет выполнить одну SQL-команду и сразу выйти из клиента MySQL. Это идеально подходит для скриптов, где вам нужно получить конкретный результат без ручного взаимодействия. Например, вы можете использовать эту опцию для написания сценария, который периодически проверяет состояние таблицы.

Итог: когда использовать расширенные опции
--default-character-set: Используйте всегда, если вы работаете с текстовыми данными, особенно в многоязычных приложениях.

--connect-timeout: Добавляйте в скрипты и автоматизированные задачи, чтобы избежать бесконечного ожидания при сбоях сети или сервера.

--show-warnings: Используйте при разработке и отладке, чтобы понять, почему ваши запросы могут вести себя не так, как вы ожидаете.

-e: Идеально подходит для скриптов, где нужно выполнить одну команду и немедленно получить результат.
