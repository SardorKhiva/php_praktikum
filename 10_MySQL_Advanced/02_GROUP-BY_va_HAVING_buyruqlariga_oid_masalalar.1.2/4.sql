USE `mashq`;

SELECT `productCode`   AS 'mahsulot kodi',     -- mahsulot kodi
SUM(`quantityOrdered`) AS 'nechtadan sotildi'  -- va nechtadan sotilganini tanlasin
FROM `orderdetails`                -- `orderdetails` jadvalidan
GROUP BY `mahsulot kodi`           -- mahsulot (kodi)larni guruhlash
ORDER BY `nechtadan sotildi` DESC  -- nechtadan sotilganini kamayish tartibida saralash
# ORDER BY `mahsulot kodi`         -- mahsulot kodi bo'yicha tartiblash
;
